function getEmptyCells(arr){
  const size = 9;
  const coords = [];
  for(let i = 0; i<size; i++){
    for(let j = 0; j<size; j++){
      if(arr[i][j] === '-'){
        coords.push([i,j]);
      }
    }
  }
  return coords;
  }
