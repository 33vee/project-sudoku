const sudoku = require('../sudoku.js');

describe('Функция solve() возвращяет поле в виде массива', () => {
  test('0', () => {
    const result = sudoku.solve('---6891--8------2915------84-3----5-2----5----9-24-8-1-847--91-5------6--6-41----');
    const object = [['-', '-', '-', '6', '8', '9', '1', '-', '-'], ['8', '-', '-', '-', '-', '-', '-', '2', '9'], ['1', '5', '-', '-', '-', '-', '-', '-', '8'], ['4', '-', '3', '-', '-', '-', '-', '5', '-'], ['2', '-', '-', '-', '-', '5', '-', '-', '-'], ['-', '9', '-', '2', '4', '-', '8', '-', '1'], ['-', '8', '4', '7', '-', '-', '9', '1', '-'], ['5', '-', '-', '-', '-', '-', '-', '6', '-'], ['-', '6', '-', '4', '1', '-', '-', '-', '-']];
    expect(result).toEqual(object);
  });
});
