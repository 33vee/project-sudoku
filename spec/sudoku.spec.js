const sudoku = require('../sudoku.js');

describe('Судоку', () => {
  it('Первая задача', () => {
    const equal1 = [
      [
        '1', '4', '5',
        '8', '9', '2',
        '6', '7', '3',
      ],
      [
        '8', '9', '3',
        '1', '7', '6',
        '4', '2', '5',
      ],
      [
        '2', '7', '6',
        '4', '3', '5',
        '8', '1', '9',
      ],
      [
        '5', '1', '9',
        '2', '4', '7',
        '3', '8', '6',
      ],
      [
        '7', '6', '2',
        '5', '8', '3',
        '1', '9', '4',
      ],
      [
        '3', '8', '4',
        '9', '6', '1',
        '7', '5', '2',
      ],
      [
        '9', '5', '7',
        '6', '1', '4',
        '2', '3', '8',
      ],
      [
        '4', '3', '8',
        '7', '2', '9',
        '5', '6', '1',
      ],
      [
        '6', '2', '1',
        '3', '5', '8',
        '9', '4', '7',
      ],
    ];
    // eslint-disable-next-line no-undef
    expect(sudoku.solve('1-58-2----9--764-52--4--819-19--73-6762-83-9-----61-5---76---3-43--2-5-16--3-89--')).toEqual(equal1);
  });

  it('Вторая задача', () => {
    const equal2 = [
      [
        '4', '7', '5',
        '9', '3', '6',
        '2', '8', '1',
      ],
      [
        '9', '3', '2',
        '8', '5', '1',
        '7', '6', '4',
      ],
      [
        '6', '8', '1',
        '2', '7', '4',
        '3', '5', '9',
      ],
      [
        '5', '1', '7',
        '4', '9', '2',
        '8', '3', '6',
      ],
      [
        '3', '4', '9',
        '7', '6', '8',
        '1', '2', '5',
      ],
      [
        '2', '6', '8',
        '3', '1', '5',
        '4', '9', '7',
      ],
      [
        '1', '5', '3',
        '6', '8', '7',
        '9', '4', '2',
      ],
      [
        '7', '9', '4',
        '5', '2', '3',
        '6', '1', '8',
      ],
      [
        '8', '2', '6',
        '1', '4', '9',
        '5', '7', '3',
      ],
    ];
    // eslint-disable-next-line no-undef
    expect(sudoku.solve('--5-3--819-285--6-6----4-5---74-283-34976---5--83--49-15--87--2-9----6---26-495-3')).toEqual(equal2);
  });
});
