function isSolved(board) {
  const res = [];
  for (let i = 0; i < board.length; i++) {
    for (let j = 0; j < board[i].length; j++) {
      if (board[i][j] === '-') {
        res.push([i, j]);
      }
    }
  }
  return res;
}

