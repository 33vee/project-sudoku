let count = 0;

let clearButton = document.querySelector('.clear_all');
let createBoard = document.querySelector('.create_board');
let solveButton = document.querySelector('.solve');

createBoard.addEventListener('click', () => {
  
  for (let i = 0; i < 81; i++) {
    tds[i].innerText = '';
    tds[i].style.fontWeight = '400';
    tds[i].style.color = '#aa400f';
  }
  for (let i = 0; i < string[count].length; i ++) {
    if(string[count][i] !== '-') {
      tds[i].innerText = string[count][i];
      tds[i].style.fontWeight = '700';
      tds[i].style.color = 'black';
    }
  }
  clearButton.style.visibility = 'visible';
  count++
})

solveButton.addEventListener('click', () => {
    let sudoku = solvedSudoku[count - 1].flat().join('')
    for(let i = 0; i < 81; i++) {
      tds[i].innerText = sudoku[i];
    }
    if(count === 5){
        count = 0;
    }
})
